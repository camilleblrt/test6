
<h3 class="toggler toggler-active">System Requirements</h3>
<div class="toggle-container">
	<ul>
		<li>PC or Mac, Windows, Linux, or Mac OS X</li>
		<li>2 USB ports or 1 USB port + 1 PS/2 port <br> - USB to PS/2 adapter
			(included)
		</li>
		<li>Media controls are not available using a PS2 connection</li>
		<li>No driver required</li>
		<li>Mac users can swap the command/control /options keys to match the Mac keyboard layout using the Mac OS X keyboard control panel</li>
	</ul>
</div>
